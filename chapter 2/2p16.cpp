#include <iostream>
using namespace std;

int main(){
	int a,b,sum,product,difference,quotient;
	cout<<"Enter first number ";
	cin>>a;
	cout<<"Enter second number ";
	cin>>b;
	sum=a+b;
	product=a*b;
	difference=a-b;
	quotient=a/b;
	cout<<"Sum of two numbers "<<sum<<endl;
	cout<<"product of two numbers "<<product<<endl;
	cout<<"Difference of two numbers "<<difference<<endl;
	cout<<"quotient of two numbers "<<quotient;
}
